let n1 = 2, n2 = 3, operacao = "/";

switch(operacao){
    case "+":
        console.log(`${n1}+${n2} = ${n1 + n2}`);
        break;
    case "-":
        console.log(`${n1}-${n2} = ${n1 - n2}`);
        break;
    case "*":
        console.log(`${n1}*${n2}= ${n1 * n2}`);
        break;
    case "/":
        console.log(`${n1}/${n2} = ${n1 / n2}`);
        break;
}